    public void realizaConexion(String idU,String passU){
        try{
            Class.forName(driver);
            try{
                con = DriverManager.getConnection(url,user,password);
                String sqlc = "SELECT id_usr,pass_usr FROM \"Usuario\";";
                
                PreparedStatement ps = con.prepareStatement(sqlc);
                ResultSet rs = ps.executeQuery();
                boolean ban = false;
                while(rs.next()){
                    String idd = rs.getString("id_usr");
                    String pass = rs.getString("pass_usr");
                    if(idU.equals(idd) && passU.equals(pass)){
                        JOptionPane.showMessageDialog(null, "login correcto");
                        ban = true;
                        break;
                    }                    
                }
                
                if(!ban){
                    JOptionPane.showMessageDialog(null, "login incorrecto");
                }else{
                    this.setVisible(false);
                    this.dispose();
                }
                
             
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }finally{
                if(con != null){
                    try {
                        con.close();
                    }catch(Exception e){
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
                
                
            }
        }catch(ClassNotFoundException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }

